# Multiple value assigned to multiple variables
x, y, z = "alpha", 1.2, 75

print("Variable x value is", x)
print("Variable y value is", y)
print("Variable z value is", z)

# Printing variable type
print(type(x))
print(type(y))
print(type(z))

print("Variable x value is", type(x))
